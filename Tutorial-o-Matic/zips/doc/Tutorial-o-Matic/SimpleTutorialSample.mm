<node TEXT="about freeplane" STYLE_REF="markdownNote" ID="ID_263962598"><richcontent TYPE="NOTE" CONTENT-TYPE="plain/">
    <text>[Freeplane](https://www.freeplane.org/wiki/index.php/Home) is a free and open source software application that supports thinking, sharing information and getting things done at work, in school and at home.

The software can be used for [mind mapping](https://secure.wikimedia.org/wikipedia/en/wiki/Mind_map) and analyzing the information contained in mind maps.

Freeplane runs on any operating system that has a current version of Java installed.

It can be run locally or [portably](https://en.wikipedia.org/wiki/Portable_application) from removable storage like an USB drive.</text>
</richcontent>
