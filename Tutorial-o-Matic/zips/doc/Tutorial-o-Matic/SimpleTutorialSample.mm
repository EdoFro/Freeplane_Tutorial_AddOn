<node TEXT="Advantages/disadvantages of Freeplane" STYLE_REF="markdownNote" ID="ID_127308749"><richcontent TYPE="NOTE" CONTENT-TYPE="plain/">
    <text>### Why use Freeplane rather than other mindmapping software?&#xd;
&#xd;
Freeplane is a mind mapping program with many overwhelming advantages and a small number of significant disadvantages when compared to similar programs.&#xd;
&#xd;
#### Advantages:&#xd;
&#xd;
* Completely free and open-source&#xd;
* 15+ years of active development&#xd;
* Frequent updates and improvements (on average, new development versions every 2 weeks)&#xd;
* Cross-platform (Win, Mac, Linux)&#xd;
* Can handle very large maps (10,000+ nodes) with good performance&#xd;
* Supports add-ons, plug-ins, and custom scripting&#xd;
* Open and well-documented API&#xd;
* You own all your data in local files with open formats&#xd;
* Extremely customizable&#xd;
* Active community&#xd;
* Allows full keyboard control&#xd;
* Supports complex formatting and styling logic&#xd;
* Supports text, number, and date/time data formats&#xd;
* Supports formulas (not just display, but actual calculations on map data)&#xd;
* Supports HTML, Markdown, and LaTex&#xd;
&#xd;
#### Disadvantages:&#xd;
&#xd;
* UI is very dated (if you can help us improve this, please contact us!)&#xd;
* No mobile client (Freeplane API now supports mind maps as an event stream; if you want to help us develop a mobile client, please contact us!)&#xd;
* Does not currently support real-time online collaborative editing (if you want to help us develop this capability, please contact us!)&#xd;
* Does not support as wide a variety of layouts as some other programs, such as flowcharts, org-charts, and Gantt chart. We are open to developing these features if users identify them as needed.&#xd;
&#xd;
Until recently, Freeplane has had one other significant disadvantage: documentation that was completely inadequate for conveying its immense functionality. Freeplane can do so much, and gives the user so many options, that it can be overwhelming. This documentation site is our ongoing attempt to address this concern!&#xd;
</text>
</richcontent>
